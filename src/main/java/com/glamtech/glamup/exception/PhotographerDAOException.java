package com.glamtech.glamup.exception;

public class PhotographerDAOException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public PhotographerDAOException() {
		super();
	}

	public PhotographerDAOException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public PhotographerDAOException(String message, Throwable cause) {
		super(message, cause);
	}

	public PhotographerDAOException(String message) {
		super(message);
	}

	public PhotographerDAOException(Throwable cause) {
		super(cause);
	}
	
	

}
