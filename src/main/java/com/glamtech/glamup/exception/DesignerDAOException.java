package com.glamtech.glamup.exception;

public class DesignerDAOException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public DesignerDAOException() {
		super();
	}

	public DesignerDAOException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public DesignerDAOException(String message, Throwable cause) {
		super(message, cause);
	}

	public DesignerDAOException(String message) {
		super(message);
	}

	public DesignerDAOException(Throwable cause) {
		super(cause);
	}
	
	

}
